package secundario;

public class Job {
    int salary;
    int dayhours;
    public Job() {
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public int getDayhours() {
        return dayhours;
    }

    public void setDayhours(int dayhours) {
        this.dayhours = dayhours;
    }
}
